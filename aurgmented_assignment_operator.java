public class aurgmented_assignment_operator {
    public static void main(String[] args) {
       // Augmented Assignment Operators
       
       int x = 10;
       int y = 3;

       // x = x + y; or x += y
       // x = x - y; or x -= y
       // x = x * y; or x *= y
       // x = x / y; or x /= y
       // x = x % y; or x %= y
        
       x += y;

       System.out.println(x);
    }
}
